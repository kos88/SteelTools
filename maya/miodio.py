from collections import defaultdict

adj = defaultdict(set)
for a, b in pairs:
    adj[a].add(b)
    adj[b].add(a)

# Endpoints have only 1 neighbour, branches have 3+ (shouldn't exist now)
endpoints = [v for v, n in adj.items() if len(n) == 1]
print("endpoints:", endpoints)
print("branches:", [v for v, n in adj.items() if len(n) > 2])

# Walk from first endpoint
start = endpoints[0]
ordered = []
prev, cur = -1, start
while True:
    ordered.append(cur)
    neighbours = adj[cur] - {prev}
    if not neighbours:
        break
    nxt = next(iter(neighbours))
    if nxt == start:
        break
    prev, cur = cur, nxt

print("ordered:", ordered)
print("length:", len(ordered))